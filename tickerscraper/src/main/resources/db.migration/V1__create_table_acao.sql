                      id BIGINT PRIMARY KEY AUTO_INCREMENT,
                      ticker VARCHAR(10),
                      nome_empresa VARCHAR(255),
                      setor VARCHAR(100),
                      segmento VARCHAR(100),
                      segmento_listagem VARCHAR(100),
                      preco_atual VARCHAR(20),
                      variacao_12m DOUBLE,
                      valor_mercado DECIMAL(20,2),
                      valor_firma DECIMAL(20,2),
                      patrimonio_liquido DECIMAL(20,2),
                      numero_total_papeis DECIMAL(20,2),
                      ativos DECIMAL(20,2),
                      ativo_circulantes DECIMAL(20,2),
                      divida_bruta DECIMAL(20,2),
                      divida_liquida DECIMAL(20,2),
                      disponibilidade DECIMAL(20,2),
                      free_float DOUBLE,
                      tag_along DOUBLE,
                      liquidez_media_diaria DECIMAL(20,2),
                      p_l DOUBLE,
                      psr DOUBLE,
                      p_vp DOUBLE,
                      dividend_yeld DOUBLE,
                      payout DOUBLE,
                      margem_liquida DOUBLE,
                      margem_bruta DOUBLE,
                      margem_ebit DOUBLE,
                      margem_ebitda DOUBLE,
                      ev_ebitda DOUBLE,
                      ev_ebit DOUBLE,
                      p_ebitda DOUBLE,
                      p_ativo DOUBLE,
                      p_capital_de_giro DOUBLE,
                      p_ativo_circulante_liquido DOUBLE,
                      vpa DOUBLE,
                      lpa DOUBLE,
                      giro_ativos DOUBLE,
                      roe DOUBLE,
                      roic DOUBLE,
                      roa DOUBLE,
                      divida_liquida_patrimonio DOUBLE,
                      divida_liquida_ebitda DOUBLE,
                      divida_liquida_ebit DOUBLE,
                      divida_bruta_patrimonio DOUBLE,
                      patrimonio_ativos DOUBLE,
                      passivos_ativos DOUBLE,
                      liquidez_corrente DOUBLE,
                      cagr_receitas_cinco_anos DOUBLE,
                      cagr_lucros_cinco_anos DOUBLE
);
