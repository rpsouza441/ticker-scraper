CREATE TABLE acao (
                      id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
                      ticker VARCHAR(10),
                      nome_empresa VARCHAR(255),
                      setor VARCHAR(100),
                      segmento VARCHAR(100),
                      segmento_listagem VARCHAR(100),
                      preco_atual VARCHAR(20),
                      variacao_12m DECIMAL,
                      valor_mercado DECIMAL(20,2),
                      valor_firma DECIMAL(20,2),
                      patrimonio_liquido DECIMAL(20,2),
                      numero_total_papeis DECIMAL(20,2),
                      ativos DECIMAL(20,2),
                      ativo_circulantes DECIMAL(20,2),
                      divida_bruta DECIMAL(20,2),
                      divida_liquida DECIMAL(20,2),
                      disponibilidade VARCHAR(100),
                      free_float DECIMAL,
                      tag_along DECIMAL,
                      liquidez_media_diaria DECIMAL(20,2),
                      p_l DECIMAL,
                      psr DECIMAL,
                      p_vp DECIMAL,
                      dividend_yeld DECIMAL,
                      payout DECIMAL,
                      margem_liquida DECIMAL,
                      margem_bruta DECIMAL,
                      margem_ebit DECIMAL,
                      margem_ebitda DECIMAL,
                      ev_ebitda DECIMAL,
                      ev_ebit DECIMAL,
                      p_ebitda DECIMAL,
                      p_ativo DECIMAL,
                      p_capital_de_giro DECIMAL,
                      p_ativo_circulante_liquido DECIMAL,
                      vpa DECIMAL,
                      lpa DECIMAL,
                      giro_ativos DECIMAL,
                      roe DECIMAL,
                      roic DECIMAL,
                      roa DECIMAL,
                      divida_liquida_patrimonio DECIMAL,
                      divida_liquida_ebitda DECIMAL,
                      divida_liquida_ebit DECIMAL,
                      divida_bruta_patrimonio DECIMAL,
                      patrimonio_ativos DECIMAL,
                      passivos_ativos DECIMAL,
                      liquidez_corrente DECIMAL,
                      cagr_receitas_cinco_anos DECIMAL,
                      cagr_lucros_cinco_anos DECIMAL,
                      dados_brutos_json JSONB, -- O tipo JSONB do PostgreSQL é otimizado para performance e busca
                      data_atualizacao TIMESTAMP WITH TIME ZONE DEFAULT NOW() -- Armazena a data com fuso horário

);
